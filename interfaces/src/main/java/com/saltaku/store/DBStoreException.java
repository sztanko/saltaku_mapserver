package com.saltaku.store;

public class DBStoreException extends Exception {

	public DBStoreException() {
		super();
	}

	public DBStoreException(String message, Throwable cause) {
		super(message, cause);
	}

	public DBStoreException(String message) {
		super(message);
	}

	public DBStoreException(Throwable cause) {
		super(cause);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = 4057444234580493284L;

}
