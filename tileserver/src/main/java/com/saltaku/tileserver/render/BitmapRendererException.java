package com.saltaku.tileserver.render;

public class BitmapRendererException extends Exception {

	public BitmapRendererException() {
	}

	public BitmapRendererException(String message) {
		super(message);
	}

	public BitmapRendererException(Throwable cause) {
		super(cause);
	}

	public BitmapRendererException(String message, Throwable cause) {
		super(message, cause);
	}

}
