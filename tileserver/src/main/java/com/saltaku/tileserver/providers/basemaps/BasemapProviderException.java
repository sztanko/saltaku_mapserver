package com.saltaku.tileserver.providers.basemaps;

public class BasemapProviderException extends Exception {

	public BasemapProviderException() {
	}

	public BasemapProviderException(String message) {
		super(message);
	}

	public BasemapProviderException(Throwable cause) {
		super(cause);
	}

	public BasemapProviderException(String message, Throwable cause) {
		super(message, cause);
	}

}
