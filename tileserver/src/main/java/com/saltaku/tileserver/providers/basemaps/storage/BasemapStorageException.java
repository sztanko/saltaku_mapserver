package com.saltaku.tileserver.providers.basemaps.storage;

public class BasemapStorageException extends Exception {

	public BasemapStorageException() {
	}

	public BasemapStorageException(String message) {
		super(message);
	}

	public BasemapStorageException(Throwable cause) {
		super(cause);
	}

	public BasemapStorageException(String message, Throwable cause) {
		super(message, cause);
	}

}
